#!/usr/bin/env node
/**
 * Walk every `mi-s:<theme>/<file>` reference inside `data/*.json` and
 * copy just those source PNGs from the gitignored
 * `public/assets/mi-singles/` symlink into the committable
 * `public/assets/mi-singles-bundle/` folder. The rest of Limezu's
 * `Theme_Sorter_Singles` stays outside the repo.
 *
 * Mirrors `bundle-pack-assets.mjs` for Modern Exteriors. Run before
 * deploying when new `mi-s:` references appear in any map JSON.
 *
 * Why copy instead of webp-encode (unlike Modern Exteriors): the
 * interior pack is small (~21MB total source), individual furniture
 * files are typically <2KB, and the runtime fetches `.png` directly
 * with no extension swap. Keeping it simple PNG-to-PNG also means
 * we don't need a sharp dep at this stage.
 *
 * Output is wiped and rebuilt every run so removed references
 * don't leave stale files in the bundle.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const SRC = path.join(ROOT, 'public/assets/mi-singles');
const DEST = path.join(ROOT, 'public/assets/mi-singles-bundle');

const keys = new Set();

// Collect every `mi-s:` string that appears anywhere in a map JSON
// (objects use them as `spriteKey`; tile cells in 16×16 furniture
// would carry them as cell strings).
function walkValue(v, visit) {
  visit(v);
  if (Array.isArray(v)) for (const item of v) walkValue(item, visit);
  else if (v && typeof v === 'object') for (const item of Object.values(v)) walkValue(item, visit);
}
const dataDir = path.join(ROOT, 'data');
for (const f of fs.readdirSync(dataDir)) {
  if (!f.endsWith('.json')) continue;
  const obj = JSON.parse(fs.readFileSync(path.join(dataDir, f), 'utf8'));
  walkValue(obj, (v) => {
    if (typeof v === 'string' && v.startsWith('mi-s:')) keys.add(v);
  });
}

// Wipe + recreate the destination so removed references don't leave
// stale files in the committed bundle.
fs.rmSync(DEST, { recursive: true, force: true });
fs.mkdirSync(DEST, { recursive: true });

let copied = 0;
let missing = 0;
const missingKeys = [];

for (const key of keys) {
  const rel = key.slice(5); // strip "mi-s:" prefix
  const src = path.join(SRC, `${rel}.png`);
  const dest = path.join(DEST, `${rel}.png`);
  if (!fs.existsSync(src)) {
    missingKeys.push(rel);
    missing++;
    continue;
  }
  fs.mkdirSync(path.dirname(dest), { recursive: true });
  fs.copyFileSync(src, dest);
  copied++;
}

// Drop a .gitkeep so the bundle folder is committable even when
// no `mi-s:` references exist yet — matters because the prod
// AssetLoader expects the folder to exist as a fetch root.
const gitkeep = path.join(DEST, '.gitkeep');
fs.writeFileSync(
  gitkeep,
  '# Auto-generated by scripts/bundle-mi-singles.mjs.\n' +
  '# Holds Modern Interiors furniture singles referenced by data/*.json.\n' +
  '# Re-run `npm run mi-s:bundle` before deploying when new mi-s: keys appear.\n',
);

console.log(`Bundled ${copied} furniture asset${copied === 1 ? '' : 's'} into public/assets/mi-singles-bundle/`);
if (missing > 0) {
  console.warn(`${missing} reference${missing === 1 ? '' : 's'} had no source PNG:`);
  for (const k of missingKeys) console.warn(`  - ${k}`);
  console.warn('Check that public/assets/mi-singles is symlinked to the Modern Interiors pack.');
}
